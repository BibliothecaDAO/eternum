import { graphql } from "../gql";

export const GET_ETERNUM_OWNER_REALM_IDS = graphql(`
  query getEternumOwnerRealmIds($accountAddress: ContractAddress!) {
    s1EternumStructureModels(where: { owner: $accountAddress }, limit: 8000) {
      edges {
        node {
          owner
          entity_id
          metadata {
            realm_id
          }
        }
      }
    }
  }
`);
