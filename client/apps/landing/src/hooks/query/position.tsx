import { graphql } from "../gql";

export const GET_ENTITY_DISTANCE = graphql(`
  query getEntityPosition($entityIds: [u32!]!) {
    s1EternumStructureModels(where: { entity_idIN: $entityIds }, limit: 8000) {
      edges {
        node {
          base {
            coord_x
            coord_y
          }
          entity_id
          entity {
            __typename
          }
        }
      }
    }
  }
`);
